# Create a data structure (i.e. a Python Class) to store movies, including movie title, box art URL (or poster URL) and a YouTube link to the movie trailer
class Movie():
    def __init__(self, movie_title, poster_image, trailer_youtube):
        self.title = movie_title
        self.poster_image_url = poster_image
        self.trailer_youtube_url = trailer_youtube
    


